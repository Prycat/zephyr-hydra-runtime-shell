T2   = "\033[36m"        # dark cyan    — body midtone
T3   = "\033[38;5;30m"   # deep teal    — shadow mass
T4   = "\033[38;5;23m"   # forest       — deepest
GR   = "\033[92m"        # bright green — eye alive
GD   = "\033[32m"        # dim green    — eye dim
WH   = "\033[97m"        # white        — edge highlight
DM   = "\033[90m"        # grey         — atmosphere / wisps
BOLD = "\033[1m"
HIDE = "\033[?25l"
SHOW = "\033[?25h"

def _enable_ansi():
    if sys.platform == "win32":
        try:
            import ctypes
            k = ctypes.windll.kernel32
            k.SetConsoleMode(k.GetStdHandle(-11), 7)
        except Exception:
            pass

def _w(s):
    sys.stdout.buffer.write(s.encode("utf-8", errors="replace"))
    sys.stdout.flush()

