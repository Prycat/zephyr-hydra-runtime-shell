# ─── Colour mapping ───────────────────────────────────────────────────────────

def _col(ch, hot):
    if ch == " ":
        return " "
    # Heavy shadow mass
    if ch == "#":
        return f"{T4}{ch}{RST}"
    # Mid density — shimmer-able
    if ch == "+":
        if ch in hot:
            return f"{WH}{ch}{RST}"
        return f"{T2}{ch}{RST}"
    # Light structure
    if ch == "-":
        return f"{T1}{ch}{RST}"
    # Atmosphere / wisps
    if ch == ".":
        return f"{DM}{ch}{RST}"
    # Fallback
    return f"{T2}{ch}{RST}"


def _render(raw, eye_bright, hot):
    out = []
    i = 0
    while i < len(raw):
        if raw[i:i+3] == "{E}":
            if eye_bright:
                out.append(f"{GR}{BOLD}@{RST}")
            else:
                out.append(f"{GD}o{RST}")
            i += 3
        else:
            out.append(_col(raw[i], hot))
            i += 1
    return "".join(out)


# ─── Reveal ───────────────────────────────────────────────────────────────────

def _reveal():
    hot = set()
    for i, line in enumerate(_DRAGON):
        _w(_render(line, eye_bright=True, hot=hot) + "\n")
        dense = sum(1 for c in line if c not in " .,;:`'")
        time.sleep(0.005 if dense < 10 else 0.015)


# ─── Eye + shimmer pulse ──────────────────────────────────────────────────────

def _pulse(cycles=4):
    n = len(_DRAGON)
    # Verify terminal supports cursor movement before pulsing
    if not sys.stdout.isatty():
        return
    for cycle in range(cycles):
        bright = (cycle % 2 == 0)
        hot = set(random.choices("ox*+=!", k=random.randint(3, 9)))
        sys.stdout.write(f"\033[{n}A")
        sys.stdout.flush()
        for line in _DRAGON:
            _w(_render(line, bright, hot) + "\n")
        time.sleep(0.20 if bright else 0.14)


# ─── Title + boot ─────────────────────────────────────────────────────────────

def _title():
    for frame in _TITLE_FRAMES:
        _w(f"\r{BOLD}{T1}{frame}{RST}")
        sys.stdout.flush()
        time.sleep(0.09)
    time.sleep(0.2)
    _w("\n")
    _w(DM)
    for ch in _SUBTITLE:
        _w(ch)
        sys.stdout.flush()
        time.sleep(0.016)
    _w(f"{RST}\n\n")
    time.sleep(0.3)

def _boot():
    for line, delay in _BOOT_LINES:
        _w(line + "\n")
        sys.stdout.flush()
        time.sleep(delay)
    _w("\n")


# ─── Entry ────────────────────────────────────────────────────────────────────

def show_splash():
    _enable_ansi()
    _w(HIDE)
    try:
        _reveal()
        _pulse(cycles=4)
        _title()
        _boot()
    except KeyboardInterrupt:
        pass
    finally:
        _w(SHOW)


if __name__ == "__main__":
    show_splash()
